#include <stdio.h>
int main(){
    //printf(format specifier, value of data)
    printf("This is all about the Data type\n");
    /* Basic Data: Basic data is that type of data which is easily define without other help. Example of Basic Data type: int, float, character and double*/
    printf("%d", 123); printf("\n");
    printf("%c", "A"); printf("\n");
    printf("%lf", 123.2344); printf("\n");
    printf("%f",12.22); printf("\n");
    /* Derived Data: Derived data is that type of data which is define to help of others..*/
    printf("%s", "Hello Nepal\n");
    return 0;

}